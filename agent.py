"""
Voltex Repair Triage Agent
agent.py — LangGraph Agentic Repair Routing

A LangGraph ReAct agent that takes a customer fault report and
routes it through a multi-step decision process:

  1. Classify the fault (product type + component)
  2. Check parts availability
  3. Check warranty/VoltCare coverage
  4. Estimate repair cost
  5. Make the final triage decision

The agent reasons at each step and decides which tool to call next.
The final output is a structured RepairBrief Pydantic model.
"""

import os
import json
import re
from typing import Optional, Literal
from pydantic import BaseModel, Field
from dotenv import load_dotenv

from langchain_anthropic import ChatAnthropic
from langgraph.prebuilt import create_react_agent

from tools import (
    classify_fault,
    check_parts,
    check_warranty,
    estimate_repair_cost,
    make_triage_decision,
)

load_dotenv()

# ─────────────────────────────────────────────
# OUTPUT SCHEMA
# ─────────────────────────────────────────────

class RepairBrief(BaseModel):
    """
    Structured repair brief generated by the triage agent.
    Sent to the repair technician before the product arrives.
    """
    # Fault identification
    product_type     : str
    fault_component  : str
    fault_description: str
    is_liquid_damage : bool

    # Routing decision
    repair_path      : Literal["tech_bar", "newark", "engineer", "replace"]
    repair_path_description: str
    urgency          : Literal["LOW", "MEDIUM", "HIGH"]

    # Coverage and cost
    coverage_type    : str
    customer_pays    : bool
    excess_applies   : bool
    excess_amount    : float
    estimated_cost_to_customer: float
    is_ber           : bool

    # Technician guidance
    technician_notes : list[str]
    escalate         : bool
    escalation_reason: str

    # Agent reasoning summary
    reasoning_summary: str = Field(
        description="2-3 sentence summary of how the agent reached this decision"
    )
    confidence       : Literal["HIGH", "MEDIUM", "LOW"]
    tools_used       : list[str]


# ─────────────────────────────────────────────
# AGENT SYSTEM PROMPT
# ─────────────────────────────────────────────

SYSTEM_PROMPT = """You are the Voltex Repair Triage Agent — an AI system that assesses customer 
fault reports and routes them to the correct repair path.

Your job is to systematically work through each fault using the available tools:

STEP 1 — Always start by calling classify_fault to identify the product type and fault component.

STEP 2 — Call check_parts to see if parts are available for the identified fault component.

STEP 3 — Call check_warranty to determine coverage based on product age and VoltCare status.

STEP 4 — Call estimate_repair_cost to calculate the total cost and check if BER threshold is exceeded.
         Use the best available part cost from check_parts and the product's current market value.

STEP 5 — Call make_triage_decision with all gathered information to produce the final routing decision.

IMPORTANT RULES:
- Always call all 5 tools in sequence — do not skip any step
- If classify_fault returns LOW confidence, note this in your reasoning
- Liquid damage always requires escalation regardless of other factors
- BER (beyond economical repair) means repair cost exceeds 65% of product value
- VoltCare Complete has a 48-hour SLA — flag this as HIGH urgency
- Never make up parts costs or availability — always use the tool results

After all tools have been called, provide a final JSON summary in this exact format:
{
  "product_type": "...",
  "fault_component": "...",
  "fault_description": "...",
  "is_liquid_damage": true/false,
  "repair_path": "tech_bar|newark|engineer|replace",
  "repair_path_description": "...",
  "urgency": "LOW|MEDIUM|HIGH",
  "coverage_type": "...",
  "customer_pays": true/false,
  "excess_applies": true/false,
  "excess_amount": 0,
  "estimated_cost_to_customer": 0,
  "is_ber": true/false,
  "technician_notes": ["..."],
  "escalate": true/false,
  "escalation_reason": "...",
  "reasoning_summary": "...",
  "confidence": "HIGH|MEDIUM|LOW",
  "tools_used": ["classify_fault", "check_parts", "check_warranty", "estimate_repair_cost", "make_triage_decision"]
}"""


# ─────────────────────────────────────────────
# AGENT CLASS
# ─────────────────────────────────────────────

class RepairTriageAgent:
    """
    LangGraph ReAct agent for repair triage.
    Initialise once, call triage() per fault report.
    """

    def __init__(self):
        self.llm = ChatAnthropic(
            model      = "claude-sonnet-4-20250514",
            api_key    = os.getenv("ANTHROPIC_API_KEY"),
            max_tokens = 4096,
        )

        self.tools = [
            classify_fault,
            check_parts,
            check_warranty,
            estimate_repair_cost,
            make_triage_decision,
        ]

        self.agent = create_react_agent(
            model  = self.llm,
            tools  = self.tools,
            prompt = SYSTEM_PROMPT,
        )

    def triage(
        self,
        fault_description    : str,
        product_type         : str,
        product_age_months   : int,
        product_value        : float,
        has_voltcare         : bool = False,
        voltcare_tier        : Optional[str] = None,
        is_accidental_damage : bool = False,
    ) -> dict:
        """
        Runs the triage agent on a fault report.

        Returns a dict with:
          - brief: RepairBrief (structured output)
          - steps: list of agent reasoning steps
          - raw_messages: full message history
        """

        user_message = f"""Please triage this repair request:

FAULT DESCRIPTION: {fault_description}
PRODUCT TYPE: {product_type}
PRODUCT AGE: {product_age_months} months
CURRENT MARKET VALUE: £{product_value:.2f}
HAS VOLTCARE: {has_voltcare}
VOLTCARE TIER: {voltcare_tier or 'N/A'}
ACCIDENTAL DAMAGE: {is_accidental_damage}

Work through this systematically using all 5 tools, then provide the final JSON summary."""

        # Run the agent
        result = self.agent.invoke({
            "messages": [{"role": "user", "content": user_message}]
        })

        messages = result["messages"]

        # Extract tool calls made
        tools_used = []
        steps      = []

        for msg in messages:
            if hasattr(msg, "tool_calls") and msg.tool_calls:
                for tc in msg.tool_calls:
                    tool_name = tc.get("name", "unknown")
                    if tool_name not in tools_used:
                        tools_used.append(tool_name)
                    steps.append({
                        "tool"  : tool_name,
                        "input" : tc.get("args", {}),
                    })
            elif hasattr(msg, "name") and msg.name:
                # Tool result message
                if steps:
                    try:
                        steps[-1]["output"] = json.loads(msg.content)
                    except Exception:
                        steps[-1]["output"] = msg.content

        # Extract final JSON from last AI message
        final_content = ""
        for msg in reversed(messages):
            if hasattr(msg, "content") and isinstance(msg.content, str):
                if "{" in msg.content:
                    final_content = msg.content
                    break

        # Parse the RepairBrief
        brief = None
        try:
            json_match = re.search(r'\{[\s\S]*\}', final_content)
            if json_match:
                parsed = json.loads(json_match.group())
                parsed["tools_used"] = tools_used
                brief = RepairBrief(**parsed)
        except Exception as e:
            # Graceful fallback
            brief = RepairBrief(
                product_type              = product_type,
                fault_component           = "unknown",
                fault_description         = fault_description,
                is_liquid_damage          = is_accidental_damage,
                repair_path               = "newark",
                repair_path_description   = "Newark Repair Centre — manual assessment required",
                urgency                   = "MEDIUM",
                coverage_type             = "unknown",
                customer_pays             = True,
                excess_applies            = False,
                excess_amount             = 0,
                estimated_cost_to_customer= 0,
                is_ber                    = False,
                technician_notes          = ["Manual assessment required — automated triage incomplete"],
                escalate                  = True,
                escalation_reason         = f"Triage error: {str(e)[:100]}",
                reasoning_summary         = "Automated triage could not complete — manual review needed.",
                confidence                = "LOW",
                tools_used                = tools_used,
            )

        return {
            "brief"   : brief,
            "steps"   : steps,
            "messages": messages,
        }


# ─────────────────────────────────────────────
# PRINT HELPER
# ─────────────────────────────────────────────

def print_brief(result: dict):
    brief = result["brief"]
    steps = result["steps"]

    urgency_icon = {"HIGH": "🔴", "MEDIUM": "🟡", "LOW": "🟢"}.get(brief.urgency, "⚪")
    path_icon    = {
        "tech_bar": "🏪",
        "newark"  : "🏭",
        "engineer": "🔧",
        "replace" : "📦",
    }.get(brief.repair_path, "❓")

    print(f"\n{'='*65}")
    print(f"VOLTEX REPAIR TRIAGE BRIEF")
    print(f"{'='*65}")
    print(f"Product:    {brief.product_type.replace('_', ' ').title()}")
    print(f"Fault:      {brief.fault_component.replace('_', ' ').title()}")
    print(f"Description:{brief.fault_description[:80]}")
    print(f"\nROUTING DECISION")
    print(f"  {path_icon} {brief.repair_path_description}")
    print(f"  {urgency_icon} Urgency: {brief.urgency}")

    if brief.escalate:
        print(f"  ⚠ ESCALATE: {brief.escalation_reason}")

    print(f"\nCOVERAGE")
    print(f"  Type:           {brief.coverage_type.replace('_', ' ').title()}")
    print(f"  Customer pays:  {'Yes' if brief.customer_pays else 'No'}")
    if brief.excess_applies:
        print(f"  Excess:         £{brief.excess_amount:.2f}")
    if brief.customer_pays:
        print(f"  Estimated cost: £{brief.estimated_cost_to_customer:.2f}")
    if brief.is_ber:
        print(f"  ⚠ BEYOND ECONOMICAL REPAIR — replacement recommended")

    if brief.technician_notes:
        print(f"\nTECHNICIAN NOTES")
        for note in brief.technician_notes:
            print(f"  • {note}")

    print(f"\nAGENT REASONING")
    print(f"  {brief.reasoning_summary}")
    print(f"  Confidence: {brief.confidence}")
    print(f"  Tools used: {' → '.join(brief.tools_used)}")

    print(f"\nAGENT STEPS ({len(steps)} tool calls)")
    for i, step in enumerate(steps, 1):
        print(f"  [{i}] {step['tool']}")

    print(f"{'='*65}")


